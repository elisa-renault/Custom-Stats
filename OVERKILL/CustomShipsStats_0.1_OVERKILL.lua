-- File Sources --
FileSource1 = "METADATA/REALITY/TABLES/INVENTORYTABLE.MBIN"

NMS_MOD_DEFINITION_CONTAINER = 
{
  ["MOD_FILENAME"]    = FileName,
  ["MOD_AUTHOR"]      = ModAuthor,
  ["LUA_AUTHOR"]      = LuaAuthor,
  ["MOD_MAINTENANCE"] = ModMaintenance,
  ["MOD_DESCRIPTION"] = Description,
  ["NMS_VERSION"]     = NMS_Version,
  ["MODIFICATIONS"]   =
  {
    {
      ["MBIN_CHANGE_TABLE"] = 
      { 
        {
          ["MBIN_FILE_SOURCE"]  = FileSource1,
          ["EXML_CHANGE_TABLE"] = 
          {
            -- InventoryTable
          }
        },
      }
    },
  }
}

local InventoryTable = NMS_MOD_DEFINITION_CONTAINER["MODIFICATIONS"][1]["MBIN_CHANGE_TABLE"][1]["EXML_CHANGE_TABLE"]

for i = 1, #BaseStatChanges do
  local ShipType = BaseStatChanges[i][1]
  local ShipStat = BaseStatChanges[i][2]
  for j = 1, #ShipStat do
    local ShipClass = ShipStat[j][1]
    local Change = ShipStat[j][2]
    for k = 1, #Change do
      local StatID = Change[k][1]
      local Min = Change[k][2][1]
      local Max = Change[k][2][2]

      InventoryTable[#InventoryTable+1] =
      {
        ["SPECIAL_KEY_WORDS"]  = {ShipType, "GcInventoryGenerationBaseStatData.xml", ShipClass, "GcInventoryGenerationBaseStatClassData.xml", "BaseStatID", StatID},
        ["VALUE_CHANGE_TABLE"] = 
        {
          {"Min", Min},
          {"Max", Max}
        }
      }

    end
  end
end